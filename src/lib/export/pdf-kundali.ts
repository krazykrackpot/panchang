// CONVERGENCE INSIGHTS
// ═══════════════════════════════════════════════════════════════════════════

function renderConvergenceInsights(doc: jsPDF, convergence: ConvergenceResult, locale: Locale) {
  let y = newPage(doc);

  y = sectionHeading(doc, 'Chart Synthesis — Convergence', y);

  const lk = (locale === 'hi' || locale === 'sa') ? 'hi' as const : 'en' as const;
  const exec = convergence.executive;

  // Overview card
  const toneLabel = exec.tone.charAt(0).toUpperCase() + exec.tone.slice(1);
  drawCard(doc, MARGIN, y - 3, CONTENT_W, 14, { fill: NAVY_CARD, border: GOLD_DARK, radius: 3 });
  doc.setFontSize(9);
  setText(doc, GOLD_LIGHT);
  doc.text(`Tone: ${toneLabel}`, MARGIN + 4, y + 2);
  doc.setFontSize(8);
  setText(doc, WHITE);
  doc.text(`Activation: ${exec.activation}/10  |  Favorability: ${exec.favorability}/10`, MARGIN + 4, y + 8);
  y += 18;

  // Key Insights
  if (exec.insights.length > 0) {
    y = subHeading(doc, 'Key Insights', y);

    for (const ins of exec.insights) {
      y = ensureSpace(doc, y, 18);
      drawCard(doc, MARGIN, y - 3, CONTENT_W, 4, { fill: NAVY_CARD, radius: 1.5 });

      doc.setFont('helvetica', 'bold');
      doc.setFontSize(7.5);
      setText(doc, GOLD_LIGHT);
      doc.text(ins.theme, MARGIN + 3, y);
      doc.setFont('helvetica', 'normal');
      drawBadge(doc, ins.temporalFrame, MARGIN + 3 + doc.getTextWidth(ins.theme) + 4, y, GOLD_DARK);
      y += 5;
      y = drawWrapped(doc, ins.summary[lk] || "", MARGIN + 3, y, CONTENT_W - 8, 7, WHITE);
      if (ins.advice[lk]) {
        y = drawWrapped(doc, `Advice: ${ins.advice[lk]}`, MARGIN + 3, y, CONTENT_W - 8, 6.5, MUTED);
      }
      y += 3;
    }
  }

  // Urgent flags
  if (exec.urgentFlags.length > 0) {
    y = ensureSpace(doc, y, 14);
    y = subHeading(doc, 'Urgent Flags', y);

    for (const flag of exec.urgentFlags) {
      y = ensureSpace(doc, y, 14);
      const sevColor = flag.severity === 3 ? RED : flag.severity === 2 ? AMBER : MUTED;
      const sevLabel = flag.severity === 3 ? 'HIGH' : flag.severity === 2 ? 'MEDIUM' : 'LOW';
      drawBadge(doc, sevLabel, MARGIN + 2, y, sevColor);
      y += 5;
      y = drawWrapped(doc, flag.message[lk] || "", MARGIN + 3, y, CONTENT_W - 6, 7, WHITE);
      y += 2;
    }
  }

  // Meta-insights
  if (exec.metaInsights.length > 0) {
    y = ensureSpace(doc, y, 14);
    y = subHeading(doc, 'Meta-Interactions', y);

    for (const mi of exec.metaInsights) {
      y = ensureSpace(doc, y, 14);
      const sevColor = mi.severity === 3 ? RED : mi.severity === 2 ? AMBER : MUTED;
      const sevLabel = mi.severity === 3 ? 'HIGH' : mi.severity === 2 ? 'MEDIUM' : 'LOW';
      drawBadge(doc, sevLabel, MARGIN + 2, y, sevColor);
      y += 5;
      y = drawWrapped(doc, mi.text[lk] || '', MARGIN + 3, y, CONTENT_W - 6, 7, WHITE);
      y += 3;
    }
  }

  addPageFooter(doc, pageNum);
}

// ═══════════════════════════════════════════════════════════════════════════
// TIPPANNI — INTERPRETIVE ANALYSIS
// ═══════════════════════════════════════════════════════════════════════════

function renderTippanni(doc: jsPDF, tippanni: TippanniContent, locale: Locale) {
  let y = newPage(doc);

  y = sectionHeading(doc, 'Tippanni — Interpretive Analysis', y);

  // ─── Personality ──────────────────────────────────────────────────
  if (tippanni.personality) {
    y = ensureSpace(doc, y, 14);
    y = subHeading(doc, 'Personality Profile', y);

    const pers = tippanni.personality;
    const blocks = [pers.lagna, pers.moonSign, pers.sunSign].filter(Boolean);

    for (const block of blocks) {
      if (!block) continue;
      y = ensureSpace(doc, y, 20);

      // Card for each personality block
      const contentLines = wrapText(doc, block.content, CONTENT_W - 10, 7);
      const cardH = Math.max(14, 8 + contentLines.length * 4.5);

      drawCard(doc, MARGIN, y - 3, CONTENT_W, cardH, { fill: NAVY_CARD, border: GOLD_DARK, radius: 2 });

      doc.setFont('helvetica', 'bold');
      doc.setFontSize(8);
      setText(doc, GOLD_LIGHT);
      doc.text(block.title, MARGIN + 4, y + 1);
      doc.setFont('helvetica', 'normal');

      doc.setFontSize(7);
      setText(doc, WHITE);
      let textY = y + 6;
      for (const line of contentLines) {
        textY = ensureSpace(doc, textY, 4.5);
        doc.text(line, MARGIN + 4, textY);
        textY += 4.5;
      }

      y += cardH + 3;
    }

    if (pers.summary) {
      y = ensureSpace(doc, y, 10);
      y = drawWrapped(doc, pers.summary, MARGIN + 2, y, CONTENT_W - 4, 7, MUTED);
      y += 4;
    }
  }

  // ─── Planet Insights ──────────────────────────────────────────────
  if (tippanni.planetInsights && tippanni.planetInsights.length > 0) {
    y = ensureSpace(doc, y, 14);
    y = subHeading(doc, 'Planet Insights', y);

    for (const pi of tippanni.planetInsights) {
      y = ensureSpace(doc, y, 22);

      // Mini card per planet
      drawCard(doc, MARGIN, y - 3, CONTENT_W, 4, { fill: NAVY_CARD, radius: 1.5 });

      // Planet name with color dot
      const pColor = hexToRgb(pi.planetColor || '#d4a853');
      setFill(doc, pColor);
      doc.circle(MARGIN + 4, y - 0.5, 1.3, 'F');

      doc.setFont('helvetica', 'bold');
      doc.setFontSize(8);
      setText(doc, GOLD_LIGHT);
      doc.text(`${pi.planetName} in ${pi.signName} (House ${pi.house})`, MARGIN + 8, y);
      doc.setFont('helvetica', 'normal');

      // Dignity badge
      if (pi.dignity) {
        const digColor = pi.dignity === 'Exalted' ? GREEN : pi.dignity === 'Debilitated' ? RED : pi.dignity === 'Own Sign' ? BLUE : AMBER;
        drawBadge(doc, pi.dignity, MARGIN + 8 + doc.getTextWidth(`${pi.planetName} in ${pi.signName} (House ${pi.house})`) + 3, y, digColor);
      }

      y += 5;
      y = drawWrapped(doc, pi.description, MARGIN + 4, y, CONTENT_W - 8, 7, WHITE);

      if (pi.implications) {
        y = drawWrapped(doc, pi.implications, MARGIN + 4, y, CONTENT_W - 8, 6.5, MUTED);
      }
      if (pi.retrogradeEffect) {
        y = drawWrapped(doc, `Retrograde: ${pi.retrogradeEffect}`, MARGIN + 4, y, CONTENT_W - 8, 6.5, AMBER);
      }
      y += 3;
    }
  }

  // ─── Yoga Insights ────────────────────────────────────────────────
  if (tippanni.yogas && tippanni.yogas.length > 0) {
    const presentYogas = tippanni.yogas.filter(yg => yg.present);
    if (presentYogas.length > 0) {
      y = ensureSpace(doc, y, 14);
      y = subHeading(doc, 'Yoga Insights', y);

      for (const yg of presentYogas) {
        y = ensureSpace(doc, y, 16);

        doc.setFont('helvetica', 'bold');
        doc.setFontSize(8);
        setText(doc, GOLD_LIGHT);
        doc.text(yg.name, MARGIN + 2, y);
        doc.setFont('helvetica', 'normal');

        // Type + Strength badges
        let bx = MARGIN + 2 + doc.getTextWidth(yg.name) + 4;
        drawBadge(doc, yg.type, bx, y, GOLD_DARK);
        bx += doc.getTextWidth(yg.type) + 8;
        const strColor = yg.strength === 'Strong' ? GREEN : yg.strength === 'Moderate' ? AMBER : RED;
        drawBadge(doc, yg.strength, bx, y, strColor);

        y += 5;
        y = drawWrapped(doc, yg.description, MARGIN + 4, y, CONTENT_W - 8, 7, WHITE);
        if (yg.implications) {
          y = drawWrapped(doc, yg.implications, MARGIN + 4, y, CONTENT_W - 8, 6.5, MUTED);
        }
        y += 3;
      }
    }
  }

  // ─── Doshas ───────────────────────────────────────────────────────
  if (tippanni.doshas && tippanni.doshas.length > 0) {
    const presentDoshas = tippanni.doshas.filter(d => d.present);
    if (presentDoshas.length > 0) {
      y = ensureSpace(doc, y, 14);
      y = subHeading(doc, 'Dosha Analysis', y);

      for (const d of presentDoshas) {
        y = ensureSpace(doc, y, 16);

        // Severity indicator
        const sevColor = d.severity === 'severe' ? RED : d.severity === 'moderate' ? AMBER : d.severity === 'mild' ? GOLD_DARK : MUTED;
        drawCard(doc, MARGIN, y - 3, CONTENT_W, 4, { fill: NAVY_CARD, radius: 1.5 });

        doc.setFont('helvetica', 'bold');
        doc.setFontSize(8);
        setText(doc, GOLD_LIGHT);
        doc.text(d.name, MARGIN + 3, y);
        doc.setFont('helvetica', 'normal');

        drawBadge(doc, d.severity.toUpperCase(), MARGIN + 3 + doc.getTextWidth(d.name) + 4, y, sevColor);

        if (d.effectiveSeverity && d.effectiveSeverity !== 'full') {
          drawBadge(doc, d.effectiveSeverity, MARGIN + 3 + doc.getTextWidth(d.name) + doc.getTextWidth(d.severity.toUpperCase()) + 14, y, BLUE);
        }

        y += 5;
        y = drawWrapped(doc, d.description, MARGIN + 4, y, CONTENT_W - 8, 7, WHITE);
        if (d.remedies) {
          y = drawWrapped(doc, `Remedies: ${d.remedies}`, MARGIN + 4, y, CONTENT_W - 8, 6.5, MUTED);
        }
        y += 3;
      }
    }
  }

  // ─── Life Areas ───────────────────────────────────────────────────
  if (tippanni.lifeAreas) {
    y = ensureSpace(doc, y, 14);
    y = subHeading(doc, 'Life Areas', y);

    const areas = tippanni.lifeAreas;
    const areaList: { label: string; data: typeof areas.career }[] = [
      { label: 'Career', data: areas.career },
      { label: 'Wealth', data: areas.wealth },
      { label: 'Marriage', data: areas.marriage },
      { label: 'Health', data: areas.health },
      { label: 'Education', data: areas.education },
    ];

    for (const { label, data } of areaList) {
      if (!data) continue;
      y = ensureSpace(doc, y, 16);

      drawCard(doc, MARGIN, y - 3, CONTENT_W, 4, { fill: NAVY_CARD, radius: 1.5 });

      doc.setFont('helvetica', 'bold');
      doc.setFontSize(8);
      setText(doc, GOLD_LIGHT);
      doc.text(label, MARGIN + 3, y);
      doc.setFont('helvetica', 'normal');

      // Rating dots
      drawRatingDots(doc, Math.round(data.rating / 2), 5, MARGIN + 35, y - 0.5);

      // Numeric rating
      doc.setFontSize(6.5);
      setText(doc, MUTED);
      doc.text(`${data.rating}/10`, MARGIN + 58, y);

      y += 5;
      y = drawWrapped(doc, data.summary, MARGIN + 4, y, CONTENT_W - 8, 7, WHITE);
      y += 2;
    }
  }

  // ─── Dasha Analysis ───────────────────────────────────────────────
  if (tippanni.dashaInsight) {
    y = ensureSpace(doc, y, 14);
    y = subHeading(doc, 'Dasha Analysis', y);

    const di = tippanni.dashaInsight;

    // Current Mahadasha card
    y = ensureSpace(doc, y, 18);
    drawCard(doc, MARGIN, y - 3, CONTENT_W, 4, { fill: NAVY_CARD, border: GOLD_DARK, radius: 2 });

    doc.setFont('helvetica', 'bold');
    doc.setFontSize(8);
    setText(doc, GOLD);
    doc.text(`Mahadasha: ${di.currentMaha}`, MARGIN + 4, y);
    doc.setFont('helvetica', 'normal');
    y += 5;
    y = drawWrapped(doc, di.currentMahaAnalysis, MARGIN + 4, y, CONTENT_W - 8, 7, WHITE);
    y += 3;

    // Current Antardasha
    y = ensureSpace(doc, y, 14);
    drawCard(doc, MARGIN, y - 3, CONTENT_W, 4, { fill: NAVY_CARD, radius: 2 });
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(8);
    setText(doc, GOLD_LIGHT);
    doc.text(`Antardasha: ${di.currentAntar}`, MARGIN + 4, y);
    doc.setFont('helvetica', 'normal');
    y += 5;
    y = drawWrapped(doc, di.currentAntarAnalysis, MARGIN + 4, y, CONTENT_W - 8, 7, WHITE);
    y += 3;

    if (di.upcoming) {
      y = ensureSpace(doc, y, 8);
      y = drawWrapped(doc, `Upcoming: ${di.upcoming}`, MARGIN + 4, y, CONTENT_W - 8, 7, MUTED);
      y += 2;
    }
  }

  // ─── Remedies ─────────────────────────────────────────────────────
  if (tippanni.remedies) {
    y = ensureSpace(doc, y, 14);
    y = subHeading(doc, 'Remedies', y);

    const rem = tippanni.remedies;

    const remedyGroups: { title: string; items: typeof rem.gemstones }[] = [
      { title: 'Gemstones', items: rem.gemstones },
      { title: 'Mantras', items: rem.mantras },
      { title: 'Practices', items: rem.practices },
    ];

    for (const group of remedyGroups) {
      if (!group.items || group.items.length === 0) continue;

      y = ensureSpace(doc, y, 14);
      doc.setFont('helvetica', 'bold');
      doc.setFontSize(8);
      setText(doc, GOLD_LIGHT);
      doc.text(group.title, MARGIN + 2, y);
      doc.setFont('helvetica', 'normal');
      y += 5;

      for (const item of group.items) {
        y = ensureSpace(doc, y, 10);
        drawCard(doc, MARGIN + 2, y - 3, CONTENT_W - 4, 4, { fill: NAVY_CARD, radius: 1 });

        doc.setFontSize(7.5);
        setText(doc, WHITE);
        doc.text(`${item.name} (${item.planet})`, MARGIN + 5, y);
        y += 4.5;
        y = drawWrapped(doc, item.description, MARGIN + 5, y, CONTENT_W - 12, 6.5, MUTED);
        y += 2;
      }
      y += 2;
    }
  }

  addPageFooter(doc, pageNum);
}

// ─── Hex to RGB helper ─────────────────────────────────────────────────────

function hexToRgb(hex: string): readonly [number, number, number] {
  const h = hex.replace('#', '');
  return [parseInt(h.substring(0, 2), 16), parseInt(h.substring(2, 4), 16), parseInt(h.substring(4, 6), 16)] as const;
}

// ═══════════════════════════════════════════════════════════════════════════
// MAIN EXPORT
// ═══════════════════════════════════════════════════════════════════════════

export function exportKundaliPDF(
  kundali: KundaliData,
  locale: Locale = 'en',
  tippanni?: TippanniContent,
): void {
  pageNum = 1;
  const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });

  // Page 1: Cover with chart diagram
  renderCoverPage(doc, kundali, locale);

  // Page 2: Planet Positions with dignity badges
  renderPlanetPositions(doc, kundali, locale);

  // Page 3: House Cusps + Key Yogas
  renderHousesAndYogas(doc, kundali, locale);

  // Page 4: Shadbala bar chart + Bhavabala
  renderShadbala(doc, kundali, locale);

  // Page 5: Dasha Timeline with visual bars
  renderDashaTimeline(doc, kundali, locale);

  // Page 6: Ashtakavarga heat map
  renderAshtakavarga(doc, kundali, locale);

  // Page 7: Divisional Charts + Jaimini
  renderDivisionalAndJaimini(doc, kundali, locale);

  // Conditional sections
  if (kundali.sadeSati) {
    renderSadeSati(doc, kundali, locale);
  }

  if (kundali.ashtakavarga?.savTable && kundali.ascendant?.sign) {
    renderTransitRadar(doc, kundali, locale);
  }

  if (tippanni?.convergence) {
    renderConvergenceInsights(doc, tippanni.convergence, locale);
  }

  // Tippanni (multi-page interpretations)
  if (tippanni) {
    renderTippanni(doc, tippanni, locale);
  }

  // Save
  const name = kundali.birthData.name.replace(/[^a-zA-Z0-9]/g, '_') || 'kundali';
  doc.save(`${name}_kundali_report.pdf`);
}
