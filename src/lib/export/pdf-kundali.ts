// ─── Page 7: Dasha Systems ──────────────────────────────────────────────────

function renderDashaSection(
  doc: jsPDF,
  title: string,
  dashas: { planetName?: { en: string; hi?: string; sa?: string }; planet?: string; signName?: { en: string; hi?: string; sa?: string }; startDate: string; endDate: string; level?: string; subPeriods?: unknown[] }[],
  locale: Locale,
  y: number,
  showAntar: boolean = false,
): number {
  y = ensureSpace(doc, y, 14);
  y = subHeading(doc, title, y);

  const mainEntries = dashas.filter(d => !('level' in d) || d.level === 'maha' || !d.level);

  mainEntries.forEach((md, idx) => {
    y = ensureSpace(doc, y, 7);
    const name = md.planetName ? t(md.planetName, locale) : (md.signName ? t(md.signName, locale) : (md.planet || '-'));

    if (idx % 2 === 0) {
      doc.setFillColor(14, 18, 45);
      doc.rect(MARGIN, y - 4, CONTENT_W, 6.5, 'F');
    }
    doc.setFontSize(8);
    doc.setTextColor(240, 212, 138);
    doc.text(name, MARGIN + 2, y);
    doc.setTextColor(155, 151, 160);
    doc.text(`${md.startDate} — ${md.endDate}`, 80, y);
    y += 6.5;

    // Antardasha sub-periods
    if (showAntar && md.subPeriods && Array.isArray(md.subPeriods)) {
      const subs = md.subPeriods as { planetName?: { en: string; hi?: string; sa?: string }; planet?: string; startDate: string; endDate: string }[];
      subs.forEach((sub) => {
        y = ensureSpace(doc, y, 6);
        const subName = sub.planetName ? t(sub.planetName, locale) : (sub.planet || '-');
        doc.setFontSize(6.5);
        doc.setTextColor(232, 230, 227);
        doc.text(`    ${subName}`, MARGIN + 6, y);
        doc.setTextColor(155, 151, 160);
        doc.text(`${sub.startDate} — ${sub.endDate}`, 80, y);
        y += 5.5;
      });
    }
  });

  return y + 4;
}

