function getPlanetDignity(p: PlanetPosition): { label: string; color: readonly [number, number, number] } | null {
  if (p.isExalted) return { label: 'Exalted', color: GREEN };
  if (p.isDebilitated) return { label: 'Debili.', color: RED };
  if (p.isOwnSign) return { label: 'Own', color: BLUE };
  return null;
}

function renderPlanetPositions(doc: jsPDF, kundali: KundaliData, locale: Locale) {
  let y = newPage(doc);

  y = sectionHeading(doc, 'Planetary Positions', y);

  // Table
  const headers = ['Planet', 'Sign', 'Degree', 'Nakshatra / Pada', 'House', 'Status'];
  const colWidths = [28, 28, 24, 46, 16, 40];
  y = drawTableHeader(doc, headers, colWidths, y);

  kundali.planets.forEach((p, idx) => {
    y = ensureSpace(doc, y, 9);

    const isEven = idx % 2 === 0;
    if (isEven) {
      setFill(doc, NAVY_ROW);
      doc.roundedRect(MARGIN, y - 4.5, CONTENT_W, 8, 0.5, 0.5, 'F');
    }

    doc.setFontSize(7);
    let x = MARGIN;

    // Planet name
    setText(doc, GOLD_LIGHT);
    doc.text(truncateToWidth(doc, t(p.planet.name, locale), colWidths[0]), x + 2, y);
    x += colWidths[0];

    // Sign
    setText(doc, WHITE);
    doc.text(truncateToWidth(doc, t(p.signName, locale), colWidths[1]), x + 2, y);
    x += colWidths[1];

    // Degree
    doc.text(truncateToWidth(doc, p.degree, colWidths[2]), x + 2, y);
    x += colWidths[2];

    // Nakshatra/Pada
    const nakPada = `${t(p.nakshatra.name, locale)} / ${p.pada}`;
    doc.text(truncateToWidth(doc, nakPada, colWidths[3]), x + 2, y);
    x += colWidths[3];

    // House
    doc.text(`${p.house}`, x + 2, y);
    x += colWidths[4];

    // Status badges
    let bx = x + 2;
    const dignity = getPlanetDignity(p);
    if (dignity) {
      bx += drawBadge(doc, dignity.label, bx, y, dignity.color) + 1.5;
    }
    if (p.isRetrograde) {
      bx += drawBadge(doc, 'Retro', bx, y, AMBER) + 1.5;
    }
    if (p.isCombust) {
      drawBadge(doc, 'Combust', bx, y, RED);
    }

    y += 8;
  });

  // Vargottama / special flags summary
  const specials = kundali.planets.filter(p => p.isVargottama || p.isMrityuBhaga || p.isPushkarNavamsha);
  if (specials.length > 0) {
    y += 4;
    y = ensureSpace(doc, y, 14);
    y = subHeading(doc, 'Special Dignities', y);
    specials.forEach(p => {
      y = ensureSpace(doc, y, 5);
      doc.setFontSize(7);
      setText(doc, MUTED);
      const flags: string[] = [];
      if (p.isVargottama) flags.push('Vargottama');
      if (p.isMrityuBhaga) flags.push('Mrityu Bhaga');
      if (p.isPushkarNavamsha) flags.push('Pushkar Navamsha');
      if (p.isPushkarBhaga) flags.push('Pushkar Bhaga');
      doc.text(`${t(p.planet.name, locale)}: ${flags.join(', ')}`, MARGIN + 2, y);
      y += 5;
    });
  }

  addPageFooter(doc, pageNum);
}

