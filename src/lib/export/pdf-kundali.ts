// ─── Page 3: House Cusps + Divisional Charts ────────────────────────────────

function renderPage3(doc: jsPDF, kundali: KundaliData, locale: Locale) {
  doc.addPage();
  pageNum++;
  addPageBackground(doc);
  let y = addContinuationHeader(doc);

  // House Cusps
  y = goldHeading(doc, 'House Cusps', y);
  const hHeaders = ['House', 'Sign', 'Degree', 'Lord'];
  const hColWidths = [30, 46, 46, 60];
  y = drawTableHeader(doc, hHeaders, hColWidths, y);

  kundali.houses.forEach((h, idx) => {
    const row = [
      `${h.house}`,
      t(h.signName, locale),
      `${h.degree.toFixed(2)}°`,
      t(h.lordName, locale),
    ];
    y = ensureSpace(doc, y, 7);
    y = drawTableRow(doc, row, hColWidths, y, idx % 2 === 0);
  });

  y += 6;

  // Divisional Charts Overview
  y = ensureSpace(doc, y, 20);
  y = goldHeading(doc, 'Divisional Charts Overview', y);

  if (kundali.divisionalCharts && Object.keys(kundali.divisionalCharts).length > 0) {
    const abbr = GRAHA_ABBREVIATIONS;
    const entries = Object.entries(kundali.divisionalCharts);

    for (const [key, chart] of entries) {
      y = ensureSpace(doc, y, 14);

      const ascSignId = chart.ascendantSign;
      const ascRashi = RASHIS.find(r => r.id === ascSignId);
      const ascName = ascRashi ? t(ascRashi.name, locale) : `Sign ${ascSignId}`;

      doc.setFontSize(8);
      doc.setTextColor(240, 212, 138);
      const label = t(chart.label, locale);
      doc.text(`${key.toUpperCase()} (${label}) — Asc: ${ascName}`, MARGIN + 2, y);
      y += 5;

      // Compact house placements
      const parts: string[] = [];
      chart.houses.forEach((planetIds, hIdx) => {
        if (planetIds.length > 0) {
          const names = planetIds.map(pid => abbr[pid] || `P${pid}`).join(',');
          parts.push(`H${hIdx + 1}: ${names}`);
        }
      });
      const line = parts.length > 0 ? parts.join(' | ') : 'No planets placed';
      const wrapped = wrapText(doc, line, CONTENT_W - 4, 7);
      doc.setFontSize(7);
      doc.setTextColor(232, 230, 227);
      for (const wl of wrapped) {
        y = ensureSpace(doc, y, 5);
        doc.text(wl, MARGIN + 4, y);
        y += 4.5;
      }
      y += 2;
    }
  } else {
    doc.setFontSize(8);
    doc.setTextColor(155, 151, 160);
    doc.text('Divisional chart data not available.', MARGIN + 2, y);
    y += 8;
  }

  addPageFooter(doc, pageNum);
}

