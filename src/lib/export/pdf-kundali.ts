// ─── Sade Sati Analysis ─────────────────────────────────────────────────────

function renderSadeSati(doc: jsPDF, kundali: KundaliData, locale: Locale) {
  doc.addPage();
  pageNum++;
  addPageBackground(doc);
  let y = addContinuationHeader(doc);

  y = goldHeading(doc, 'Sade Sati Analysis', y);

  const ss = kundali.sadeSati!;
  const lk = (locale === 'hi' || locale === 'sa') ? 'hi' as const : 'en' as const;

  // Status
  if (ss.isActive) {
    const phaseLabel = ss.currentPhase === 'rising' ? 'Rising' : ss.currentPhase === 'peak' ? 'Peak' : 'Setting';
    y = subHeading(doc, `Status: ACTIVE — ${phaseLabel} Phase`, y);
    doc.setFontSize(8);
    doc.setTextColor(232, 230, 227);
    doc.text(`Cycle: ${ss.cycleStart} — ${ss.cycleEnd}  |  Progress: ${Math.round(ss.phaseProgress * 100)}%`, MARGIN + 2, y);
    y += 7;

    // Intensity
    y = ensureSpace(doc, y, 12);
    y = subHeading(doc, `Intensity: ${ss.overallIntensity.toFixed(1)} / 10`, y);

    if (ss.intensityFactors.length > 0) {
      ss.intensityFactors.forEach(f => {
        y = ensureSpace(doc, y, 5);
        doc.setFontSize(7);
        doc.setTextColor(155, 151, 160);
        doc.text(`  ${f.description[lk]}  —  ${f.score.toFixed(0)}/10`, MARGIN + 2, y);
        y += 4.5;
      });
      y += 3;
    }
  } else {
    y = subHeading(doc, 'Status: Not Active', y);
    const nextCycle = ss.allCycles.find(c => !c.isActive && c.startYear > new Date().getFullYear());
    if (nextCycle) {
      doc.setFontSize(8);
      doc.setTextColor(232, 230, 227);
      doc.text(`Next cycle: ${nextCycle.startYear} — ${nextCycle.endYear}`, MARGIN + 2, y);
      y += 7;
    }
  }

  // Summary interpretation
  const summary = ss.interpretation.summary[lk];
  if (summary) {
    y = ensureSpace(doc, y, 15);
    y = subHeading(doc, 'Summary', y);
    y = drawWrappedText(doc, summary, y, CONTENT_W - 4, 7.5, TEXT_PRIMARY);
    y += 4;
  }

  // Saturn nature + moon strength (if active)
  if (ss.isActive) {
    const saturnText = ss.interpretation.saturnNature[lk];
    if (saturnText) {
      y = ensureSpace(doc, y, 15);
      y = subHeading(doc, "Saturn's Nature for Ascendant", y);
      y = drawWrappedText(doc, saturnText, y, CONTENT_W - 4, 7.5, TEXT_PRIMARY);
      y += 4;
    }
    const moonText = ss.interpretation.moonStrength[lk];
    if (moonText) {
      y = ensureSpace(doc, y, 15);
      y = subHeading(doc, 'Moon Strength', y);
      y = drawWrappedText(doc, moonText, y, CONTENT_W - 4, 7.5, TEXT_PRIMARY);
      y += 4;
    }
  }

  // Timeline — compact
  if (ss.allCycles.length > 0) {
    y = ensureSpace(doc, y, 15);
    y = subHeading(doc, 'Timeline', y);
    ss.allCycles.forEach(cycle => {
      y = ensureSpace(doc, y, 5);
      doc.setFontSize(7.5);
      const activePhase = cycle.isActive && ss.currentPhase ? ` [ACTIVE - ${ss.currentPhase.charAt(0).toUpperCase() + ss.currentPhase.slice(1)}]` : '';
      if (cycle.isActive) {
        doc.setTextColor(212, 168, 83);
      } else {
        doc.setTextColor(155, 151, 160);
      }
      doc.text(`${cycle.startYear} — ${cycle.endYear}${activePhase}`, MARGIN + 2, y);
      y += 5;
    });
    y += 3;
  }

  // Remedies (if active)
  if (ss.isActive && ss.remedies.length > 0) {
    y = ensureSpace(doc, y, 15);
    y = subHeading(doc, 'Remedies', y);

    for (const r of ss.remedies) {
      y = ensureSpace(doc, y, 12);
      const priorityTag = `[${r.priority.toUpperCase()}]`;
      doc.setFontSize(7.5);
      doc.setTextColor(240, 212, 138);
      doc.text(`${priorityTag} ${r.title[lk]}`, MARGIN + 2, y);
      y += 4.5;
      y = drawWrappedText(doc, r.description[lk], y, CONTENT_W - 8, 7, TEXT_SECONDARY);
      y += 2;
    }
  }

  addPageFooter(doc, pageNum);
}

// ─── Transit Radar ─────────────────────────────────────────────────────────

function renderTransitRadar(doc: jsPDF, kundali: KundaliData, locale: Locale) {
  doc.addPage();
  pageNum++;
  addPageBackground(doc);
  let y = addContinuationHeader(doc);

  y = goldHeading(doc, 'Current Transit Analysis', y);

  const ascSign = kundali.ascendant.sign;
  const savTable = kundali.ashtakavarga?.savTable;

  if (!savTable || !ascSign) {
    doc.setFontSize(8);
    doc.setTextColor(155, 151, 160);
    doc.text('Transit analysis requires Ashtakavarga data. Not available for this chart.', MARGIN + 2, y);
    y += 8;
    addPageFooter(doc, pageNum);
    return;
  }

  const transits = computePersonalTransits(ascSign, savTable);

  if (transits.length === 0) {
    doc.setFontSize(8);
    doc.setTextColor(155, 151, 160);
    doc.text('No transit data computed.', MARGIN + 2, y);
    y += 8;
    addPageFooter(doc, pageNum);
    return;
  }

  // Transit table
  const tHeaders = ['Planet', 'Sign', 'House', 'SAV Bindus', 'Quality'];
  const tColWidths = [36, 36, 24, 30, 56];
  y = drawTableHeader(doc, tHeaders, tColWidths, y);

  transits.forEach((tr, idx) => {
    y = ensureSpace(doc, y, 7);
    const qualityLabel = tr.quality === 'strong' ? 'Favorable'
      : tr.quality === 'weak' ? 'Challenging' : 'Moderate';
    const row = [
      t(tr.planetName, locale),
      t(tr.signName, locale),
      `${tr.house}`,
      `${tr.savBindu}`,
      qualityLabel,
    ];

    // Custom row with quality color
    const isEven = idx % 2 === 0;
    if (isEven) {
      doc.setFillColor(14, 18, 45);
      doc.rect(MARGIN, y - 4, CONTENT_W, 6.5, 'F');
    }
    doc.setFontSize(7);
    doc.setTextColor(232, 230, 227);
    let x = MARGIN;
    row.forEach((cell, ci) => {
      if (ci === 4) {
        // Color the quality text
        if (tr.quality === 'strong') {
          doc.setTextColor(100, 200, 120); // green
        } else if (tr.quality === 'weak') {
          doc.setTextColor(220, 100, 100); // red
        } else {
          doc.setTextColor(212, 168, 83); // gold for neutral
        }
      }
      doc.text(cell.substring(0, Math.floor(tColWidths[ci] / 1.8)), x + 1, y);
      if (ci === 4) {
        doc.setTextColor(232, 230, 227); // reset
      }
      x += tColWidths[ci];
    });
    y += 6.5;
  });

  // Interpretations
  y += 4;
  y = ensureSpace(doc, y, 14);
  y = subHeading(doc, 'Transit Interpretations', y);

  transits.forEach(tr => {
    y = ensureSpace(doc, y, 10);
    const interp = t(tr.interpretation, locale);
    y = drawWrappedText(doc, interp, y, CONTENT_W - 4, 7, TEXT_PRIMARY);
    y += 2;
  });

  // Upcoming transitions
  const upcoming = computeUpcomingTransitions();
  if (upcoming.length > 0) {
    y += 4;
    y = ensureSpace(doc, y, 20);
    y = subHeading(doc, 'Upcoming Sign Changes', y);

    const uHeaders = ['Planet', 'From', 'To', 'Approx. Date'];
    const uColWidths = [40, 40, 40, 62];
    y = drawTableHeader(doc, uHeaders, uColWidths, y);

    upcoming.forEach((u, idx) => {
      y = ensureSpace(doc, y, 7);
      const row = [
        t(u.planetName, locale),
        t(u.fromSign, locale),
        t(u.toSign, locale),
        u.approximateDate,
      ];
      y = drawTableRow(doc, row, uColWidths, y, idx % 2 === 0);
    });
  }

  addPageFooter(doc, pageNum);
}

