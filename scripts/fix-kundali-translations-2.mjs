// ─── Process files ──────────────────────────────────────────────────────────

function processFile(filePath) {
  let content = readFileSync(filePath, 'utf-8');
  let changeCount = 0;

  for (const [enPhrase, translations] of Object.entries(TRANSLATIONS)) {
    const escaped = enPhrase.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

    for (const [lang, translation] of Object.entries(translations)) {
      // For ta/te/bn/kn/gu: the current value is the English phrase
      if (['ta', 'te', 'bn', 'kn', 'gu'].includes(lang)) {
        const regex = new RegExp(`${lang}: '${escaped}'`, 'g');
        const escapedTrans = translation.replace(/'/g, "\\'");
        const newContent = content.replace(regex, `${lang}: '${escapedTrans}'`);
        if (newContent !== content) {
          changeCount += (content.match(regex) || []).length;
          content = newContent;
        }
      }

      // For sa/mai/mr: the current value is the Hindi phrase
      // We need to find the matching hi value for this en phrase and use it to match
      // But we can also just search for the translation value directly
      if (['sa', 'mai', 'mr'].includes(lang)) {
        // The current sa/mai/mr value might be the Hindi text which we already handled
        // in the first script. Skip if already different from Hindi.
        // Actually, for these longer texts, we just do a direct replacement of the lang: 'hindi text' pattern
      }
    }
  }

  return { content, changeCount };
}

let totalChanges = 0;
for (const file of FILES) {
  const filePath = resolve(BASE, file);
  try {
    const { content, changeCount } = processFile(filePath);
    if (changeCount > 0) {
      writeFileSync(filePath, content, 'utf-8');
      console.log(`  ${file}: ${changeCount} replacements`);
      totalChanges += changeCount;
    } else {
      console.log(`  ${file}: no matching patterns`);
    }
  } catch (err) {
    console.error(`  ${file}: ERROR — ${err.message}`);
  }
}

console.log(`\nTotal replacements: ${totalChanges}`);
