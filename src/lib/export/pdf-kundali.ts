// PAGE 5: DASHA TIMELINE
// ═══════════════════════════════════════════════════════════════════════════

function renderDashaTimeline(doc: jsPDF, kundali: KundaliData, locale: Locale) {
  let y = newPage(doc);

  y = sectionHeading(doc, 'Dasha Timeline', y);

  if (!kundali.dashas || kundali.dashas.length === 0) {
    doc.setFontSize(8);
    setText(doc, MUTED);
    doc.text('Dasha data not available.', MARGIN + 2, y);
    addPageFooter(doc, pageNum);
    return;
  }

  // Find current Maha Dasha
  const today = new Date().toISOString().split('T')[0];
  const mahas = kundali.dashas.filter(d => !d.level || d.level === 'maha');

  // ─── Current Dasha highlight box ──────────────────────────────────
  const currentMaha = mahas.find(d => d.startDate <= today && d.endDate >= today);
  if (currentMaha) {
    y = ensureSpace(doc, y, 22);
    drawCard(doc, MARGIN, y - 4, CONTENT_W, 18, { fill: NAVY_CARD, border: GOLD, radius: 3 });
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(9);
    setText(doc, GOLD);
    const mahaName = currentMaha.planetName ? t(currentMaha.planetName, locale) : (currentMaha.planet || '-');
    doc.text(`Current Mahadasha: ${mahaName}`, MARGIN + 4, y + 2);
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(7.5);
    setText(doc, WHITE);
    doc.text(`${currentMaha.startDate}  to  ${currentMaha.endDate}`, MARGIN + 4, y + 9);
    y += 22;
  }

  // ─── Visual timeline bars ─────────────────────────────────────────
  y = ensureSpace(doc, y, 14);
  y = subHeading(doc, 'Mahadasha Periods', y);
  y += 2;

  // Calculate total span for proportional widths
  const barAreaW = CONTENT_W - 40; // leave room for labels

  // Parse dates for proportional bar widths
  const dashaSpans = mahas.map(d => {
    const start = new Date(d.startDate).getTime();
    const end = new Date(d.endDate).getTime();
    return { ...d, startMs: start, endMs: end, durationMs: end - start };
  });
  const totalMs = dashaSpans.reduce((sum, d) => sum + d.durationMs, 0);

  dashaSpans.forEach(d => {
    y = ensureSpace(doc, y, 10);

    const name = d.planetName ? t(d.planetName, locale) : (d.planet || '-');
    const isCurrent = d.startDate <= today && d.endDate >= today;

    // Planet label
    doc.setFontSize(7);
    setText(doc, isCurrent ? GOLD : MUTED);
    doc.text(name, MARGIN + 2, y + 3);

    // Proportional bar
    const barW = Math.max(4, (d.durationMs / totalMs) * barAreaW);
    const barColor = isCurrent ? GOLD : GOLD_DARK;
    drawBar(doc, MARGIN + 22, y, barW, barAreaW, barColor);

    // Date range
    doc.setFontSize(5.5);
    setText(doc, MUTED);
    const dateLabel = `${d.startDate.substring(0, 7)} - ${d.endDate.substring(0, 7)}`;
    doc.text(dateLabel, MARGIN + 22, y + 7);

    y += 10;
  });

  // ─── Current Antar Dasha sub-periods ──────────────────────────────
  if (currentMaha && currentMaha.subPeriods && currentMaha.subPeriods.length > 0) {
    y += 4;
    y = ensureSpace(doc, y, 14);
    y = subHeading(doc, 'Antardasha (Current Mahadasha Sub-Periods)', y);

    const aHeaders = ['Antardasha', 'Start', 'End'];
    const aColWidths = [60, 61, 61];
    y = drawTableHeader(doc, aHeaders, aColWidths, y);

    const subs = currentMaha.subPeriods as { planetName?: { en: string; hi?: string; sa?: string }; planet?: string; startDate: string; endDate: string }[];
    subs.forEach((sub, idx) => {
      y = ensureSpace(doc, y, 8);
      const subName = sub.planetName ? t(sub.planetName, locale) : (sub.planet || '-');
      const isCurrAntar = sub.startDate <= today && sub.endDate >= today;

      if (isCurrAntar) {
        // Highlight current
        setFill(doc, [20, 25, 55]);
        doc.roundedRect(MARGIN, y - 4.5, CONTENT_W, 8, 0.5, 0.5, 'F');
        setDraw(doc, GOLD_DARK);
        doc.setLineWidth(0.2);
        doc.roundedRect(MARGIN, y - 4.5, CONTENT_W, 8, 0.5, 0.5, 'S');
      } else if (idx % 2 === 0) {
        setFill(doc, NAVY_ROW);
        doc.roundedRect(MARGIN, y - 4.5, CONTENT_W, 8, 0.5, 0.5, 'F');
      }

      doc.setFontSize(7);
      setText(doc, isCurrAntar ? GOLD_LIGHT : WHITE);
      doc.text(truncateToWidth(doc, subName, aColWidths[0]), MARGIN + 2, y);
      setText(doc, isCurrAntar ? WHITE : MUTED);
      doc.text(sub.startDate, MARGIN + aColWidths[0] + 2, y);
      doc.text(sub.endDate, MARGIN + aColWidths[0] + aColWidths[1] + 2, y);

      y += 8;
    });
  }

  // ─── Additional Dasha Systems (compact) ───────────────────────────
  const additionalDashas: { label: string; data: typeof kundali.dashas }[] = [];
  if (kundali.yoginiDashas?.length) additionalDashas.push({ label: 'Yogini Dasha', data: kundali.yoginiDashas });
  if (kundali.ashtottariDashas?.length) additionalDashas.push({ label: 'Ashtottari Dasha', data: kundali.ashtottariDashas });

  for (const { label, data } of additionalDashas) {
    y += 6;
    y = ensureSpace(doc, y, 14);
    y = subHeading(doc, label, y);

    const entries = data.filter(d => !d.level || d.level === 'maha');
    entries.forEach((d, idx) => {
      y = ensureSpace(doc, y, 6);
      const name = d.planetName ? t(d.planetName, locale) : (d.planet || '-');

      if (idx % 2 === 0) {
        setFill(doc, NAVY_ROW);
        doc.roundedRect(MARGIN, y - 4, CONTENT_W, 6.5, 0.5, 0.5, 'F');
      }
      doc.setFontSize(7);
      setText(doc, GOLD_LIGHT);
      doc.text(name, MARGIN + 2, y);
      setText(doc, MUTED);
      doc.text(`${d.startDate}  —  ${d.endDate}`, MARGIN + 50, y);
      y += 6.5;
    });
  }

  addPageFooter(doc, pageNum);
}

