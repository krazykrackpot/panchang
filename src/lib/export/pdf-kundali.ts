// ─── Main Export Function ───────────────────────────────────────────────────

export function exportKundaliPDF(
  kundali: KundaliData,
  locale: Locale = 'en',
  tippanni?: TippanniContent,
): void {
  pageNum = 1;
  const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });

  // Page 1: Cover + Birth Details + Planet Positions
  renderPage1(doc, kundali, locale);

  // Page 2: Graha Details (Extended)
  renderPage2(doc, kundali, locale);

  // Page 3: House Cusps + Divisional Charts
  renderPage3(doc, kundali, locale);

  // Page 4: Yogas
  renderPage4(doc, kundali, locale);

  // Page 5: Shadbala
  renderPage5(doc, kundali, locale);

  // Page 6: Bhavabala
  renderPage6(doc, kundali, locale);

  // Page 7: Dasha Systems
  renderPage7(doc, kundali, locale);

  // Page 8: Ashtakavarga
  renderPage8(doc, kundali, locale);

  // Page 9: Jaimini System
  renderPage9(doc, kundali, locale);

  // Sade Sati Analysis
  if (kundali.sadeSati) {
    renderSadeSati(doc, kundali, locale);
  }

  // Transit Radar
  if (kundali.ashtakavarga?.savTable && kundali.ascendant?.sign) {
    renderTransitRadar(doc, kundali, locale);
  }

  // Convergence Insights
  if (tippanni?.convergence) {
    renderConvergenceInsights(doc, tippanni.convergence, locale);
  }

  // Page 10+: Tippanni (Interpretations)
  if (tippanni) {
    renderTippanni(doc, tippanni, locale);
  }

  // Save PDF
  const name = kundali.birthData.name.replace(/[^a-zA-Z0-9]/g, '_') || 'kundali';
  doc.save(`${name}_kundali_report.pdf`);
}
