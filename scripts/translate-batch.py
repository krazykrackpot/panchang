def process_file(fpath):
    with open(fpath, 'r') as f:
        content = f.read()
    original = content
    changed = 0
    for en_val, (ta, te, bn, kn, gu) in TERMS.items():
        escaped = re.escape(en_val)
        # Replace ta: 'ENGLISH' with ta: 'TRANSLATED' (only when it matches exactly)
        for locale_key, translated in [('ta', ta), ('te', te), ('bn', bn), ('kn', kn), ('gu', gu)]:
            pattern = f"{locale_key}: '{escaped}'"
            replacement = f"{locale_key}: '{translated}'"
            if pattern in content:
                content = content.replace(pattern, replacement)
                changed += 1
    if content != original:
        with open(fpath, 'w') as f:
            f.write(content)
        real_changes = changed // 5  # Each term has 5 locale replacements
        print(f"  OK {fpath} — {real_changes} terms translated")
    return 1 if content != original else 0

files = sys.argv[1:] if len(sys.argv) > 1 else sorted(
    glob.glob('src/**/*.tsx', recursive=True) + glob.glob('src/**/*.ts', recursive=True)
)
files = [f for f in files if 'node_modules' not in f and '.test.' not in f and 'messages/' not in f]
total = sum(process_file(f) for f in files)
print(f"\nDone: {total} files updated")
