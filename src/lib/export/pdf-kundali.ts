// ─── State ─────────────────────────────────────────────────────────────────
let pageNum = 1;

// ─── Trilingual Helper ─────────────────────────────────────────────────────
function t(obj: { en: string; hi?: string; sa?: string } | undefined, locale: Locale): string {
  if (!obj) return '-';
  return (obj as Record<string, string>)[locale] || obj.en || '-';
}

// ─── Color Helpers ─────────────────────────────────────────────────────────
function setFill(doc: jsPDF, c: readonly [number, number, number]) {
  doc.setFillColor(c[0], c[1], c[2]);
}
function setText(doc: jsPDF, c: readonly [number, number, number]) {
  doc.setTextColor(c[0], c[1], c[2]);
}
function setDraw(doc: jsPDF, c: readonly [number, number, number]) {
  doc.setDrawColor(c[0], c[1], c[2]);
}

// ─── Page Infrastructure ───────────────────────────────────────────────────

function addPageBackground(doc: jsPDF) {
  setFill(doc, NAVY);
  doc.rect(0, 0, PAGE_W, PAGE_H, 'F');
  // Thin gold border inset
  setDraw(doc, GOLD_DARK);
  doc.setLineWidth(0.3);
  doc.rect(8, 8, PAGE_W - 16, PAGE_H - 16);
}

function addPageFooter(doc: jsPDF, pn: number) {
  // Thin rule
  setDraw(doc, GOLD_DARK);
  doc.setLineWidth(0.15);
  doc.line(MARGIN, 280, PAGE_W - MARGIN, 280);
  doc.setFontSize(6.5);
  setText(doc, MUTED);
  doc.text('Generated by Dekho Panchang  |  dekhopanchang.com', PAGE_W / 2, 284, { align: 'center' });
  doc.text(`${pn}`, PAGE_W - MARGIN - 2, 284, { align: 'right' });
}

function addContinuationHeader(doc: jsPDF): number {
  doc.setFont('times', 'italic');
  doc.setFontSize(9);
  setText(doc, GOLD_DARK);
  doc.text('Dekho Panchang — Kundali Report', PAGE_W / 2, 18, { align: 'center' });
  doc.setFont('helvetica', 'normal');
  setDraw(doc, GOLD_DARK);
  doc.setLineWidth(0.15);
  doc.line(40, 21, PAGE_W - 40, 21);
  return 30;
}

function ensureSpace(doc: jsPDF, y: number, needed: number): number {
  if (y + needed > MAX_Y) {
    addPageFooter(doc, pageNum);
    doc.addPage();
    pageNum++;
    addPageBackground(doc);
    return addContinuationHeader(doc);
  }
  return y;
}

function newPage(doc: jsPDF): number {
  addPageFooter(doc, pageNum);
  doc.addPage();
  pageNum++;
  addPageBackground(doc);
  return addContinuationHeader(doc);
}

// ─── Drawing Primitives ────────────────────────────────────────────────────

function drawCard(
  doc: jsPDF, x: number, y: number, w: number, h: number,
  opts: { fill?: readonly [number, number, number]; border?: readonly [number, number, number]; radius?: number } = {},
) {
  const r = opts.radius ?? 2;
  if (opts.fill) {
    setFill(doc, opts.fill);
    doc.roundedRect(x, y, w, h, r, r, 'F');
  }
  if (opts.border) {
    setDraw(doc, opts.border);
    doc.setLineWidth(0.3);
    doc.roundedRect(x, y, w, h, r, r, 'S');
  }
}

function drawBadge(
  doc: jsPDF, text: string, x: number, y: number,
  color: readonly [number, number, number],
) {
  doc.setFontSize(6);
  const tw = doc.getTextWidth(text) + 4;
  const h = 4.5;
  // Badge background (semi-transparent via darker shade)
  setFill(doc, [Math.round(color[0] * 0.3), Math.round(color[1] * 0.3), Math.round(color[2] * 0.3)]);
  doc.roundedRect(x, y - 3.2, tw, h, 1.2, 1.2, 'F');
  // Badge border
  setDraw(doc, color);
  doc.setLineWidth(0.2);
  doc.roundedRect(x, y - 3.2, tw, h, 1.2, 1.2, 'S');
  // Text
  setText(doc, color);
  doc.text(text, x + 2, y);
  return tw;
}

function drawBar(
  doc: jsPDF, x: number, y: number, value: number, maxWidth: number,
  color: readonly [number, number, number],
) {
  const barW = Math.max(2, Math.min(value, maxWidth));
  // Bar background track
  setFill(doc, NAVY_CARD);
  doc.roundedRect(x, y, maxWidth, 4, 1, 1, 'F');
  // Bar fill
  setFill(doc, color);
  doc.roundedRect(x, y, barW, 4, 1, 1, 'F');
}

function drawRatingDots(
  doc: jsPDF, rating: number, maxRating: number, x: number, y: number,
) {
  const dotR = 1.2;
  const gap = 4;
  for (let i = 0; i < maxRating; i++) {
    const cx = x + i * gap + dotR;
    if (i < rating) {
      setFill(doc, GOLD);
    } else {
      setFill(doc, GOLD_DARK);
    }
    doc.circle(cx, y, dotR, 'F');
  }
}

// ─── Text Helpers ──────────────────────────────────────────────────────────

function truncateToWidth(doc: jsPDF, text: string, maxWidth: number): string {
  if (doc.getTextWidth(text) <= maxWidth - 2) return text;
  let truncated = text;
  while (truncated.length > 0 && doc.getTextWidth(truncated + '...') > maxWidth - 2) {
    truncated = truncated.slice(0, -1);
  }
  return truncated + '...';
}

function wrapText(doc: jsPDF, text: string, maxWidth: number, fontSize: number): string[] {
  doc.setFontSize(fontSize);
  const words = text.split(' ');
  const lines: string[] = [];
  let line = '';
  for (const word of words) {
    const test = line ? `${line} ${word}` : word;
    if (doc.getTextWidth(test) > maxWidth) {
      if (line) lines.push(line);
      line = word;
    } else {
      line = test;
    }
  }
  if (line) lines.push(line);
  return lines.length > 0 ? lines : [''];
}

function drawWrapped(
  doc: jsPDF, text: string, x: number, y: number, maxWidth: number,
  fontSize: number, color: readonly [number, number, number],
): number {
  setText(doc, color);
  const lines = wrapText(doc, text, maxWidth, fontSize);
  for (const line of lines) {
    y = ensureSpace(doc, y, 4.5);
    doc.text(line, x, y);
    y += 4.5;
  }
  return y;
}

// ─── Section Headings ──────────────────────────────────────────────────────

function sectionHeading(doc: jsPDF, text: string, y: number): number {
  y = ensureSpace(doc, y, 12);
  doc.setFont('times', 'bold');
  doc.setFontSize(13);
  setText(doc, GOLD);
  doc.text(text, MARGIN, y);
  // Gold underline
  setDraw(doc, GOLD);
  doc.setLineWidth(0.4);
  doc.line(MARGIN, y + 2.5, MARGIN + CONTENT_W, y + 2.5);
  doc.setFont('helvetica', 'normal');
  return y + 10;
}

function subHeading(doc: jsPDF, text: string, y: number): number {
  y = ensureSpace(doc, y, 8);
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(9.5);
  setText(doc, GOLD_LIGHT);
  doc.text(text, MARGIN + 1, y);
  doc.setFont('helvetica', 'normal');
  return y + 6;
}

// ─── Table Drawing ─────────────────────────────────────────────────────────

function drawTableHeader(doc: jsPDF, headers: string[], colWidths: number[], y: number): number {
  setFill(doc, NAVY_HDR);
  doc.roundedRect(MARGIN, y - 4.5, CONTENT_W, 8, 1, 1, 'F');
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(7);
  setText(doc, GOLD);
  let x = MARGIN;
  headers.forEach((h, i) => {
    doc.text(truncateToWidth(doc, h, colWidths[i]), x + 2, y);
    x += colWidths[i];
  });
  doc.setFont('helvetica', 'normal');
  return y + 7;
}

function drawTableRow(doc: jsPDF, cells: string[], colWidths: number[], y: number, isEven: boolean): number {
  if (isEven) {
    setFill(doc, NAVY_ROW);
    doc.roundedRect(MARGIN, y - 4, CONTENT_W, 8, 0.5, 0.5, 'F');
  }
  doc.setFontSize(7);
  setText(doc, WHITE);
  let x = MARGIN;
  cells.forEach((cell, i) => {
    doc.text(truncateToWidth(doc, cell, colWidths[i]), x + 2, y);
    x += colWidths[i];
  });
  return y + 8;
}

// ─── North Indian Chart ────────────────────────────────────────────────────

function drawNorthChart(
  doc: jsPDF,
  chartData: { houses: number[][]; ascendantSign: number },
  x: number, y: number, size: number,
) {
  const cx = x + size / 2;
  const cy = y + size / 2;
  const half = size / 2;

  // Outer square
  setDraw(doc, GOLD);
  doc.setLineWidth(0.6);
  doc.rect(x, y, size, size);

  // Diamond (inner rotated square)
  doc.setLineWidth(0.4);
  // Top to Right
  doc.line(cx, y, x + size, cy);
  // Right to Bottom
  doc.line(x + size, cy, cx, y + size);
  // Bottom to Left
  doc.line(cx, y + size, x, cy);
  // Left to Top
  doc.line(x, cy, cx, y);

  // Cross lines for inner triangles
  // Top-left: horizontal and vertical
  doc.setLineWidth(0.25);
  setDraw(doc, GOLD_DARK);
  // Top horizontal
  doc.line(x, y, x + size, y);
  doc.line(x, y + size, x + size, y + size);
  doc.line(x, y, x, y + size);
  doc.line(x + size, y, x + size, y + size);

  // Inner divider lines for the 12 houses
  // House corners (North Indian diamond style):
  // The diamond divides outer square corners into triangles
  // Additional lines from midpoints
  const mx = cx;    // midpoints
  const my = cy;

  // Top-left to mid-left, mid-top
  doc.line(x, y, mx, my);       // diagonal TL to center — already drawn
  doc.line(x + size, y, mx, my); // diagonal TR to center
  doc.line(x + size, y + size, mx, my); // diagonal BR to center
  doc.line(x, y + size, mx, my); // diagonal BL to center

  // North Indian standard house layout (Asc in top diamond = house 1)
  // House positions relative to center:
  // The 12 houses in North Indian chart go clockwise from top center
  // House 1 = top center diamond
  // House mapping: standard positions for text placement
  const q = size / 4;
  const housePositions: [number, number][] = [
    [cx, y + q * 0.9],              // H1 — top center
    [x + q * 0.7, y + q * 0.7],    // H12 — top-left triangle
    [x + q * 0.7, cy],              // H11 — left-top
    [x + q * 0.7, cy + q * 0.9],   // H10 — left-bottom
    [x + q * 0.7, y + size - q * 0.6], // H9 — bottom-left triangle
    [cx, y + size - q * 0.8],       // H8 — bottom center
    [x + size - q * 0.7, y + size - q * 0.6], // H7 — bottom-right triangle
    [x + size - q * 0.7, cy + q * 0.9],       // H6 — right-bottom
    [x + size - q * 0.7, cy],       // H5 — right-top
    [x + size - q * 0.7, y + q * 0.7], // H4 — top-right triangle
    [cx + q * 1.1, y + q * 0.9],   // H3 — (top-right of center)
    [cx - q * 1.1, y + q * 0.9],   // H2 — (top-left of center)
  ];

  // Reorder: index 0=H1, so actual house numbers:
  // N. Indian: positions go 1(top), 12(upper-left), 11(left-upper), 10(left-lower),
  //            9(lower-left), 8(bottom), 7(lower-right), 6(right-lower),
  //            5(right-upper), 4(upper-right), 3, 2
  // Standard mapping: housePositions[i] is for house (arrangement[i])
  const houseOrder = [1, 12, 11, 10, 9, 8, 7, 6, 5, 4, 3, 2];

  const abbr = GRAHA_ABBREVIATIONS;

  doc.setFontSize(5);
  setText(doc, WHITE);

  houseOrder.forEach((houseNum, posIdx) => {
    // chartData.houses is 0-indexed (houses[0] = house 1 planets)
    const planetsInHouse = chartData.houses[houseNum - 1] || [];
    if (planetsInHouse.length === 0) return;

    const labels = planetsInHouse.map(pid => abbr[pid] || `P${pid}`);
    const text = labels.join(' ');
    const [px, py] = housePositions[posIdx];
    doc.text(truncateToWidth(doc, text, q * 1.5), px, py, { align: 'center' });
  });

  // "Asc" label in house 1
  doc.setFontSize(4.5);
  setText(doc, GOLD_LIGHT);
  doc.text('Asc', cx, y + q * 0.4, { align: 'center' });
}

