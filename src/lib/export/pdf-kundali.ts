// ─── Theme Constants ───────────────────────────────────────────────────────
const NAVY       = [10, 14, 39]   as const;  // #0a0e27
const NAVY_CARD  = [14, 18, 50]   as const;  // card fill
const NAVY_ROW   = [14, 18, 45]   as const;  // alternating rows
const NAVY_HDR   = [17, 22, 56]   as const;  // table header
const GOLD       = [212, 168, 83] as const;  // #d4a853
const GOLD_LIGHT = [240, 212, 138] as const; // #f0d48a
const GOLD_DARK  = [138, 109, 43] as const;  // #8a6d2b
