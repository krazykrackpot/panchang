function addContinuationHeader(doc: jsPDF): number {
  doc.setFontSize(11);
  doc.setTextColor(212, 168, 83);
  doc.text('Dekho Panchang — Kundali Report (continued)', PAGE_W / 2, 22, { align: 'center' });
  doc.setDrawColor(212, 168, 83);
  doc.setLineWidth(0.2);
  doc.line(30, 26, 180, 26);
  return 34;
}

function ensureSpace(doc: jsPDF, y: number, needed: number): number {
  if (y + needed > MAX_Y) {
    addPageFooter(doc, pageNum);
    doc.addPage();
    pageNum++;
    addPageBackground(doc);
    return addContinuationHeader(doc);
  }
  return y;
}

function goldHeading(doc: jsPDF, text: string, y: number): number {
  doc.setFontSize(13);
  doc.setTextColor(212, 168, 83);
  doc.text(text, MARGIN, y);
  doc.setDrawColor(212, 168, 83);
  doc.setLineWidth(0.2);
  doc.line(MARGIN, y + 2, MARGIN + CONTENT_W, y + 2);
  return y + 8;
}

function subHeading(doc: jsPDF, text: string, y: number): number {
  doc.setFontSize(10);
  doc.setTextColor(240, 212, 138);
  doc.text(text, MARGIN, y);
  return y + 6;
}

function drawTableHeader(doc: jsPDF, headers: string[], colWidths: number[], y: number): number {
  doc.setFillColor(17, 22, 56);
  doc.rect(MARGIN, y - 4, CONTENT_W, 7, 'F');
  doc.setFontSize(7.5);
  doc.setTextColor(212, 168, 83);
  let x = MARGIN;
  headers.forEach((h, i) => {
    doc.text(h, x + 1, y);
    x += colWidths[i];
  });
  return y + 7;
}

function drawTableRow(doc: jsPDF, cells: string[], colWidths: number[], y: number, isEven: boolean): number {
  if (isEven) {
    doc.setFillColor(14, 18, 45);
    doc.rect(MARGIN, y - 4, CONTENT_W, 6.5, 'F');
  }
  doc.setFontSize(7);
  doc.setTextColor(232, 230, 227);
  let x = MARGIN;
  cells.forEach((cell, i) => {
    doc.text(cell.substring(0, Math.floor(colWidths[i] / 1.8)), x + 1, y);
    x += colWidths[i];
  });
  return y + 6.5;
}

function wrapText(doc: jsPDF, text: string, maxWidth: number, fontSize: number): string[] {
  doc.setFontSize(fontSize);
  const words = text.split(' ');
  const lines: string[] = [];
  let line = '';
  for (const word of words) {
    const test = line ? `${line} ${word}` : word;
    if (doc.getTextWidth(test) > maxWidth) {
      if (line) lines.push(line);
      line = word;
    } else {
      line = test;
    }
  }
  if (line) lines.push(line);
  return lines;
}

function drawWrappedText(doc: jsPDF, text: string, y: number, maxWidth: number, fontSize: number, color: string): number {
  const [r, g, b] = hexToRgb(color);
  doc.setTextColor(r, g, b);
  const lines = wrapText(doc, text, maxWidth, fontSize);
  for (const line of lines) {
    y = ensureSpace(doc, y, 5);
    doc.text(line, MARGIN + 2, y);
    y += 4.5;
  }
  return y;
}

function hexToRgb(hex: string): [number, number, number] {
  const h = hex.replace('#', '');
  return [parseInt(h.substring(0, 2), 16), parseInt(h.substring(2, 4), 16), parseInt(h.substring(4, 6), 16)];
}

