function findEnContentAbove(lines, maiLineIdx) {
  for (let j = maiLineIdx - 1; j >= Math.max(0, maiLineIdx - 10); j--) {
    const m = lines[j].match(/^\s*en:\s*'(.+?)'/);
    if (m) return m[1];
  }
  return null;
}

function injectTranslations(src) {
  const lines = src.split('\n');
  const result = [];

  for (let i = 0; i < lines.length; i++) {
    result.push(lines[i]);

    const maiMatch = lines[i].match(/^(\s*)mai:\s*'(.+?)'\s*,?\s*$/);
    if (maiMatch) {
      // Check if next line already has ta:
      if (i + 1 < lines.length && lines[i + 1].match(/^\s*ta:/)) {
        continue; // Already has translations
      }

      const indent = maiMatch[1];
      const enContent = findEnContentAbove(lines, i);
      if (enContent) {
        for (const [key, trans] of Object.entries(T)) {
          if (enContent.includes(key.replace(/\\'/g, "'"))) {
            result.push(`${indent}ta: '${trans.ta}',`);
            result.push(`${indent}te: '${trans.te}',`);
            result.push(`${indent}bn: '${trans.bn}',`);
            result.push(`${indent}kn: '${trans.kn}',`);
            result.push(`${indent}gu: '${trans.gu}',`);
            break;
          }
        }
      }
    }
  }

  return result.join('\n');
}

